'use strict';

const INITIALIZATION_VECTOR = 171;

function toUTF8Array(str) {
    var utf8 = [];
    for (var i=0; i < str.length; i++) {
        var charcode = str.charCodeAt(i);
        if (charcode < 0x80) utf8.push(charcode);
        else if (charcode < 0x800) {
            utf8.push(0xc0 | (charcode >> 6),
                      0x80 | (charcode & 0x3f));
        }
        else if (charcode < 0xd800 || charcode >= 0xe000) {
            utf8.push(0xe0 | (charcode >> 12),
                      0x80 | ((charcode>>6) & 0x3f),
                      0x80 | (charcode & 0x3f));
        }
        // surrogate pair
        else {
            i++;
            // UTF-16 encodes 0x10000-0x10FFFF by
            // subtracting 0x10000 and splitting the
            // 20 bits of 0x0-0xFFFFF into two halves
            charcode = 0x10000 + (((charcode & 0x3ff)<<10)
                      | (str.charCodeAt(i) & 0x3ff));
            utf8.push(0xf0 | (charcode >>18),
                      0x80 | ((charcode>>12) & 0x3f),
                      0x80 | ((charcode>>6) & 0x3f),
                      0x80 | (charcode & 0x3f));
        }
    }
    return utf8;
}

module.exports = {
  encrypt: function(content) {
    var buf = new Buffer(4+content.length);
    buf.writeInt32BE(content.length, 0);
    var utf8Array = toUTF8Array(content);
    var key = INITIALIZATION_VECTOR;
    utf8Array.forEach(function(value, i) {
      var cipher = key ^ value;
      console.log('char = '+cipher+ ' i='+i+ ' buf = ' + buf.values());
      buf.writeInt16BE(cipher, i); // Need Int8
      key = cipher;
    });
    return buf;
  }
}
